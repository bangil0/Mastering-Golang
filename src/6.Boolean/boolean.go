package main

import (
	"fmt"
)

func main() {
	fmt.Println(7 > 2)  // Return True
	fmt.Println(7 < 2)  // Return False
	fmt.Println(8 == 8) // Return True
	fmt.Println(8 == 2) // Return False
	fmt.Println(8 != 2) // Return True
	fmt.Println(3 <= 3) // Return True
	fmt.Println(4 >= 4) // Return True
}
